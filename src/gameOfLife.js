export const createGrid = (height, width) => new Array(height)
  .fill(0)
  .map(() => new Array(width).fill(0));

const getNeighbors = (cells, x, y) => {
  let neighbors = 0;
  for (let i = -1; i <= 1; i++) {
    const ny = y + i;
    for (let j = -1; j <= 1; j++) {
      const nx = x + j;

      // eslint-disable-next-line no-continue
      if (x === nx && y === ny) continue;
      if (cells[nx] && cells[nx][ny] === 1) neighbors += 1;
    }
  }

  return neighbors;
};

const dieAlone = (cells) => (x, y) => {
  if (cells[y][x] === 0) return false;
  return getNeighbors(cells, y, x) < 2;
};

const dieOverpopulation = (cells) => (x, y) => {
  if (cells[y][x] === 0) return false;
  return getNeighbors(cells, y, x) > 3;
};

const bornOverpopulation = (cells) => (x, y) => {
  if (cells[y][x] === 1) return false;
  return getNeighbors(cells, y, x) === 3;
};

const clone = (cells) => cells.map((e) => [].concat(e));

const when = (...fns) => (...params) => {
  const fn = fns.find(([fx]) => fx(...params));
  return fn && fn[1] ? fn[1](...params) : false;
};

const game = (cells) => {
  const _cells = clone(cells);
  const patternMatch = when(
    [dieAlone(cells), (x, y, c) => c[y][x] = 0],
    [dieOverpopulation(cells), (x, y, c) => c[y][x] = 0],
    [bornOverpopulation(cells), (x, y, c) => c[y][x] = 1],
  );

  return cells.reduce((result, row, y) => {
    row.forEach((_, x) => {
      patternMatch(x, y, result);
    });
    return result;
  }, _cells);
};

export default game;
